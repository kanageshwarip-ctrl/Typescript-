let browser = "c"

switch(browser){
case "c" :
    console.log(" chrome browser");
    break;
case "f" :
    console.log("firefox browser");
    break;
case "e" :
    console.log("edge browser");
    break;
case "s" :
    console.log("safari browser");
    break;
default :
    console.log("invalid browser");    
}